package lesson6.objects;

public class Pupy extends  dog{
    private String colorEyes;

    public Pupy() {
        super(name);
        this.colorEyes = colorEyes;
    }

    public Pupy(String name, boolean agry, String colorEyes) {
        super(name, agry);
        this.colorEyes = colorEyes;
    }

    public Pupy(String colorEyes) {
        super();
        this.colorEyes = colorEyes;
    }
}
